  __HAL_RCC_GPIOB_CLK_ENABLE();

  /*Configure GPIO pin Output Level */
  HAL_GPIO_WritePin(nRESET_GPIO_Port, nRESET_Pin, GPIO_PIN_SET);

  /*Configure GPIO pin Output Level */
  HAL_GPIO_WritePin(GPIOB, SPI_CS_Pin|SYS_RLED_Pin|SYS_BLED_Pin, GPIO_PIN_SET);

  /*Configure GPIO pin Output Level */
  HAL_GPIO_WritePin(GPIOB, nSLEEP_Pin|DRV_EN_Pin|Test_PIN_Pin, GPIO_PIN_RESET);

  /*Configure GPIO pin : PtPin */
